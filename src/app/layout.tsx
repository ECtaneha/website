interface LanguageContextType {
  language: string;
  setLanguage: React.Dispatch<React.SetStateAction<string>>;
}

export const CurrentLanguage = createContext<LanguageContextType>({
  language: '日本語',
  setLanguage: () => {},
});

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const pathname = usePathname();

  const [language, setLanguage] = useState<string>(() => {
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('selectedLanguage');
      return savedLanguage || '日本語';
    } else {
      return '日本語';
    }
  });

  useEffect(() => {
    localStorage.setItem('selectedLanguage', language);
  }, [language]);

  return (
    <html lang="jp">
      {/* <body className={inter.className}> */}
      <head>
        <title>株式会社タイシン</title>
      </head>
      <body>
        <CurrentLanguage.Provider value={{ language, setLanguage }}>
          <CssBaseline />
          <Header />
          <Suspense fallback={<div>Loading...</div>}>
            {children}
          </Suspense>
          <ToTopButton />
          {pathname !== '/Admin/DashBoard' && <Footer />}
        </CurrentLanguage.Provider>
      </body>
    </html>
  );
}
